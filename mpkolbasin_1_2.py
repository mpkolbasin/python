a = int(input("Введите время в секундах:"))
if a<=60:
    print(f"Время: 00:00:{a:02d}")
elif a > 61 and a <= 3599:
     b = a // 60
     c = a % 60
     print(f"Время: 00:{b:02d}:{c:02d}")
elif a >= 3600 and a <= 86399:
     d = a // 3600
     b = (a - d * 3600) // 60
     c = a % 60
     print(f"Время: {d:02.0f}:{b:02d}:{c:02d}")
else:
    print("Неверно задано число")